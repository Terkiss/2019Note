package ArrayTest;

public class ArrayExmple {
	public static void main(String[] args) {
	{
		String[]  oldStringArray = {"JAVA", "ARRYA"};
		String[] NewStringArrgs = new String[5];
		
		System.arraycopy(oldStringArray, 0, NewStringArrgs, 0, oldStringArray.length);
		
		for(int i = 0; i < NewStringArrgs.length; i++)
		{
			System.out.print(NewStringArrgs[i]);
		}
		
		int inArray[][] = {{ 90, 90, 110, 110}, {120, 110, 100, 110}, {120, 140, 130, 150} };
		
		double sum = 0;
		int loopCount = 0;
		int i =0;
		
		while(true)
		{
			if(i < inArray.length)
			{
				break;
			}
			int namuge = loopCount%inArray[0].length;
			if( namuge == 0 )
			{
				i++;
			}
			sum += inArray[i][namuge];
			loopCount++;
		}
		
	}
	
	}
}
